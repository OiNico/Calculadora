
class Mat:
    
    #soma
    def Soma(primeiroValor, segundoValor):
        return primeiroValor + segundoValor
    
    #subtração
    def Subtracao(primeiroValor, segundoValor):
        return primeiroValor - segundoValor
    
    #multiplicação
    def Multiplicacao(primeiroValor, segundoValor):
        return primeiroValor * segundoValor
    
    #divisão
    def divisao(primeiroValor, segundoValor):
        return primeiroValor / segundoValor
    
